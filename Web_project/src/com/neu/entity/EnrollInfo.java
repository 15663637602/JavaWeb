package com.neu.entity;

public class EnrollInfo {
	int e_id;
	String e_name;
	String stu_id;
	String e_company;
	String e_position;
	String e_location;
	String e_salary;
	public int getE_id() {
		return e_id;
	}
	public void setE_id(int e_id) {
		this.e_id = e_id;
	}
	public String getStu_id() {
		return stu_id;
	}
	public void setStu_id(String stu_id) {
		this.stu_id = stu_id;
	}
	public String getE_company() {
		return e_company;
	}
	public void setE_company(String e_company) {
		this.e_company = e_company;
	}
	public String getE_position() {
		return e_position;
	}
	public void setE_position(String e_position) {
		this.e_position = e_position;
	}
	public String getE_location() {
		return e_location;
	}
	public void setE_location(String e_location) {
		this.e_location = e_location;
	}
	public String getE_salary() {
		return e_salary;
	}
	public void setE_salary(String e_salary) {
		this.e_salary = e_salary;
	}
	public String getE_name() {
		return e_name;
	}
	public void setE_name(String e_name) {
		this.e_name = e_name;
	}
	
}
