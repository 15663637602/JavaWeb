package com.neu.entity;


public class RecommendInfo {
	private int rec_id;
	private String rec_title;
	private String rec_link;
	public int getRec_id() {
		return rec_id;
	}
	public void setRec_id(int rec_id) {
		this.rec_id = rec_id;
	}
	public String getRec_title() {
		return rec_title;
	}
	public void setRec_title(String rec_title) {
		this.rec_title = rec_title;
	}
	public String getRec_link() {
		return rec_link;
	}
	public void setRec_link(String rec_link) {
		this.rec_link = rec_link;
	}
	
	
	
}